package com.wise.ds.repository;

public class CubeMember {
    private int cubeId;
    private int dsViewId;
    private String name;
    private String description;
    private int order;
    public int getCubeId() {
        return cubeId;
    }
    public void setCubeId(int cubeId) {
        this.cubeId = cubeId;
    }
    public int getDsViewId() {
        return dsViewId;
    }
    public void setDsViewId(int dsViewId) {
        this.dsViewId = dsViewId;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getDescription() {
        return description;
    }
    public void setDescription(String description) {
        this.description = description;
    }
    public int getOrder() {
        return order;
    }
    public void setOrder(int order) {
        this.order = order;
    }
    @Override
    public String toString() {
        return "Cube [cubeId=" + cubeId + ", dsViewId=" + dsViewId + ", name=" + name + ", description=" + description + ", order=" + order + "]";
    }
    
    
}
