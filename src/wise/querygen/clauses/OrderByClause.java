package wise.querygen.clauses;

import wise.querygen.dto.*;

public class OrderByClause {

	 public String FieldName;
     public Sorting SortOrder;
     
     public OrderByClause(String field)
     {
         FieldName = field;
         SortOrder = Sorting.Ascending;
     }
     public OrderByClause(String field, Sorting order)
     {
         FieldName = field;
         SortOrder = order;
     }
     
	public String getFieldName() {
		return FieldName;
	}
	public void setFieldName(String fieldName) {
		FieldName = fieldName;
	}
	public Sorting getSortOrder() {
		return SortOrder;
	}
	public void setSortOrder(Sorting sortOrder) {
		SortOrder = sortOrder;
	}
     
     
}
