package wise.querygen.dto;

public class SelectCube extends DataTable {

	private String UNI_NM;
	private String CAPTION;
	private String DATA_TYPE;
	private String TYPE;
	private String ORDER;
	public String getUNI_NM() {
		return UNI_NM;
	}
	public void setUNI_NM(String uNI_NM) {
		UNI_NM = uNI_NM;
	}
	public String getCAPTION() {
		return CAPTION;
	}
	public void setCAPTION(String cAPTION) {
		CAPTION = cAPTION;
	}
	public String getDATA_TYPE() {
		return DATA_TYPE;
	}
	public void setDATA_TYPE(String dATA_TYPE) {
		DATA_TYPE = dATA_TYPE;
	}
	public String getTYPE() {
		return TYPE;
	}
	public void setTYPE(String tYPE) {
		TYPE = tYPE;
	}
	public String getORDER() {
		return ORDER;
	}
	public void setORDER(String oRDER) {
		ORDER = oRDER;
	}
	
	@Override
	public String toString() {
		return "SelectCube [UNI_NM=" + UNI_NM + ", CAPTION=" + CAPTION + ", DATA_TYPE=" + DATA_TYPE + ", TYPE=" + TYPE
				+ ", ORDER=" + ORDER + "]";
	}
	
}
